# Crie um script Python que leia dois números e tente mostrar a soma entre eles.
numero01 = int(input('\033[33mQual é o primeiro número?\033[m'))
numero02 = int(input('\033[34mQual é o segundo número?\033[m'))
soma = numero01 + numero02
print(f'A soma dos dois números é igual a \033[1;31m{soma}\033[m.')
